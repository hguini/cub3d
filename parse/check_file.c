#include "../cub3d.h"

int		check_file(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i + 1] == '.')
		{
			if (str[i + 2] == 'c' &&
				str[i + 3] == 'u' &&
				str[i + 4] == 'b' &&
				str[i + 5] == '\0')
				return (1);
		}
		i++;
	}
	return (0);
}
